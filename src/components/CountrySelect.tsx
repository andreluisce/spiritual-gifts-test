function flagFromCode(code: string) {
  if (!code || code.length !== 2) return 'ðŸ³ï¸'
  const base = 127397
  const chars = [...code.toUpperCase()].map((c) => base + c.charCodeAt(0))
  return String.fromCodePoint(...chars)
}

type CountrySelectProps = {
  value?: string
  onChange: (value: string) => void
  placeholder?: string
  disabled?: boolean
}

export function CountrySelect({ value, onChange, placeholder, disabled }: CountrySelectProps) {
  const [open, setOpen] = useState(false)

  const sortedCountries = useMemo(
    () => [...COUNTRIES].sort((a, b) => a.name.localeCompare(b.name)),
    []
  )

  const selected = sortedCountries.find(
    (country) =>
      country.name.toLowerCase() === (value || '').toLowerCase() ||
      country.code.toLowerCase() === (value || '').toLowerCase()
  )

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        <Button
          variant="outline"
          role="combobox"
          className="w-full justify-between"
          disabled={disabled}
        >
          {selected ? (
            <span className="flex items-center gap-2 truncate">
              <span className="text-lg">{flagFromCode(selected.code)}</span>
              <span className="truncate">{selected.name}</span>
            </span>
          ) : (
            <span className="text-muted-foreground">{placeholder || 'Select country'}</span>
          )}
          <ChevronsUpDown className="ml-2 h-4 w-4 shrink-0 opacity-50" />
        </Button>
      </PopoverTrigger>
      <PopoverContent className="w-[320px] p-0">
        <Command>
          <CommandInput placeholder="Search country..." />
          <CommandList>
            <CommandEmpty>No country found.</CommandEmpty>
            <CommandGroup>
              {sortedCountries.map((country) => {
                const flag = flagFromCode(country.code)
                const isSelected = selected?.code === country.code
                return (
                  <CommandItem
                    key={country.code}
                    value={`${country.name} ${country.code}`}
                    onSelect={() => {
                      onChange(country.code)
                      setOpen(false)
                    }}
                    className="flex items-center gap-2"
                  >
                    <span className="text-lg">{flag}</span>
                    <span className="flex-1 truncate">{country.name}</span>
                    {isSelected && <Check className="h-4 w-4 opacity-60" />}
                  </CommandItem>
                )
              })}
            </CommandGroup>
          </CommandList>
        </Command>
      </PopoverContent>
    </Popover>
  )
}

export function resolveCountryLabel(value?: string) {
  if (!value) return ''
  const match = COUNTRIES.find(
    (country) =>
      country.code.toLowerCase() === value.toLowerCase() ||
      country.name.toLowerCase() === value.toLowerCase()
  )
  return match ? match.name : value
}
